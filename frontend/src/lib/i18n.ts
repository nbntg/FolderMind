const dict = { 'zh-CN': zh, en };

export function t(language: Language, key: keyof typeof zh): string {
  return dict[language][key] ?? zh[key];
}

export function formatT(language: Language, key: keyof typeof zh, replacements: Record<string, string | number>): string {
  let value = t(language, key);
  for (const [name, replacement] of Object.entries(replacements)) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }
  return value;
}
